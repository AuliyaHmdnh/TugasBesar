    private void buttonUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUbahActionPerformed
       try{
             String sql = "UPDATE gaji SET id='"+textID.getText()+"',nama='"+textNama.getText()+"',jabatan='"+cbJab.getSelectedItem()+"',gaji='"+textGaji.getText()+"',jam='"+textJam.getText()+"',tunjangan='"+textTun.getText()+"',bonus='"+textBonus.getText()+"',potongan='"+textPot.getText()+"',gaji_bersih='"+textBersih.getText()+"' WHERE id ='"+textID.getText()+"'";
             java.sql.Connection conn = (Connection)config.configDB();
             java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
             pstm.execute();
             JOptionPane.showMessageDialog(null, "Edit Data Berhasil..");
         }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        try {
            tampilkan_data();
        } catch (SQLException ex) {
            Logger.getLogger(Gaji.class.getName()).log(Level.SEVERE, null, ex);
        }
         kosongkan();
    }//GEN-LAST:event_buttonUbahActionPerformed

    private void buttonHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHitungActionPerformed
        double gj = Double.parseDouble(textGaji.getText());
        double jk = Double.parseDouble(textJam.getText());
        
        double tj;
        if(gj >= 1000000){
            tj = 100000;
        }else if (gj >= 1500000) {
            tj = 150000;
        }else if (gj >= 2000000) {
            tj = 200000;
        }else{
            tj = 0;
        }
        
        double bns;
        if(jk >= 72) {
            bns = 200000;
        }else{
            bns= 0;
        }
            
        double pot;
        if(jk <= 48) {
            pot = 100000;
        }else{
            pot = 0;
        }
        double gjb = gj+tj+bns;
        
        textTun.setText(String.valueOf(tj));
        textBersih.setText(String.valueOf(gjb));
        textBonus.setText(String.valueOf(bns));
        textPot.setText(String.valueOf(pot));
    }//GEN-LAST:event_buttonHitungActionPerformed

