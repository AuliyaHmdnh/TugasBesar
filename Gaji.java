    private void tampilkan_data() throws SQLException{
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("Jabatan");
        model.addColumn("Gaji");
        model.addColumn("Jam Kerja");
        model.addColumn("Tunjangan");
        model.addColumn("Bonus");
        model.addColumn("Potongan");
        model.addColumn("Gaji Bersih");
        
        try{
            int no = 1;
            String sql = " SELECT * FROM gaji ";
            java.sql.Connection conn = (Connection)config.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6),res.getString(7),res.getString(8),res.getString(9)});
            }
            tabelGaji.setModel(model);
            
        }catch (SQLException e){
            System.out.println("Error : " + e.getMessage());
        }
    }
    
    
